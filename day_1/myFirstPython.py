#Python Simple Calculations:
print(3 + 4)                  # addition(+)
print(4 - 3)                  # substraction(-)
print(4 * 3)                  # multiplication(*)
print(4 / 3)                  # division(/)
print(3 ** 4)                 # exponential(**)
print(4 % 3)                  # output: 1 (modulus (which means finding the remainder (ভাগশেষ))
print(7 // 3)                 # output: 2 (floor division operator i.e.removing the remainder(ভাগফল))
print(13 % 3)                 # output: 1

#Python data types:
print(type(20))                                               # integer(i.e. ...-3,-2,-1,0,1,2,3..)
print(type(20.5))                                             # float
print(type(1+3j))                                             # complex
print(type("Alifa"))                                          # string
print(type([1,3,6,8,'Bangladesh',0.1]))                       # list:    ordered collection of different data types in the list (int, string, float);
print(type({'name' : 'Alifa', 'country': 'Bangladesh'}))      # dictionary:    ('key':'ValuePair' format) & unordered collections;
print(type(("alifa", "Bangladesh", "book")))                  # tuple (ordered collection of different data types)
print(type({3 ,14, 4.3, "Alifa"}))                            # set  (order isn't important)
print(type(True))                                             # boolean