def generate_full_name(firstname, lastname):
    space = ' '
    fullname = firstname + space + lastname
    return fullname


def sum_two_nums(num1, num2):
    result = num1 + num2
    return result


def weight(mass, gravity):
    weight = mass * gravity
    return weight
